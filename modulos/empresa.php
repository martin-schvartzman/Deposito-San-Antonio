<p style="text-align:left;">Depósito San Antonio es una empresa de origen 
familiar que se dedica al procesamiento integral 
de distintos tipos de materiales especialmente 
<b>chatarra</b>, <b>hierro</b>, <b>acero</b>, <b>metales</b> y cualquier forma
 de <b>materiales ferrosos</b> (<b>perfiles</b>, <b>caño</b>, <b>tubo</b>, 
 <b>chapa</b>, <b>varillas</b>, <b>alambre</b>, <b>lata</b>, etc). Contamos
 con muchos años de experiencia en el rubro de 
 la chatarra y brindamos un servicio integral en 
 su procesamiento. Incluso trabajamos también con
 materiales no ferrosos recuperables como <b>plástico</b>, 
 <b>papel</b>, <b>vidrio</b> y <b>madera</b> (pallets) participando de 
 su proceso de <b>reciclado</b>. Brindamos servicios 
 relacionados a la <b>chatarra</b>, <b>metales</b> y <b>máquinas</b> 
 (<b>demolición</b>, <b>desguace industrial</b> o <b>naval</b> y <b>desarme</b> de 
 <b>galpones</b>, <b>estructuras</b>, <b>tanques</b>,etc) y ofrecemos la 
 <b>compra y venta</b> de todas las <b>máquinas en desuso</b> ya 
 sean <b>industriales, viales, agrícolas</b>, etc.</p><br/>
